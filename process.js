// process: Provides information and control over the current Node.js process.

console.log(process.pid); // Returns the process ID
console.log(process.platform); // Returns the platform (e.g., 'win32', 'linux')
console.log(process.cwd()); // Returns the current working directory
console.log(process.memoryUsage()); // Returns memory usage information
console.log(process.env); // Returns environment variables